<?php
// Определение массива с температурой
$temperatureArray = array(
    "понедельник" => 25,
    "вторник" => 26,
    "среда" => 24,
    "четверг" => 27,
    "пятница" => 28,
    "суббота" => 23,
    "воскресенье" => 22
);

// Вывод массива с использованием цикла foreach
foreach ($temperatureArray as $day => $temperature) {
    echo "Температура в городе в " . $day . " " . $temperature . " градусов<br>";
}
?>
